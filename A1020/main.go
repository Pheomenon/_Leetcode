func findContentChildren(g []int, s []int) int {
	sort.Ints(g)
	sort.Ints(s)
	result := 0
	i := 0
	j := 0
	for ; i < len(g); i++ {
		for ; j < len(s); j++ {
			if g[i] > s[j] {
				continue
			} else if g[i] <= s[j] {
				result++
				if i < len(g)-1 {
					i++
				} else {
					return result
				}
			}
		}
	}
	return result
}
